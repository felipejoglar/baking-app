package com.fjoglar.bakingapp.data.source.remote.jsonmodel;

import android.os.Parcel;
import android.os.Parcelable;

import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;

import java.util.Objects;

/**
 * Immutable model class for a recipe JsonStep.
 */
public class JsonStep implements Parcelable {

    @SerializedName("id")
    @Expose
    private int id;
    @SerializedName("shortDescription")
    @Expose
    private String shortDescription;
    @SerializedName("description")
    @Expose
    private String description;
    @SerializedName("videoURL")
    @Expose
    private String videoURL;
    @SerializedName("thumbnailURL")
    @Expose
    private String thumbnailURL;

    public JsonStep(int id, String shortDescription, String description,
                    String videoURL, String thumbnailURL) {
        this.id = id;
        this.shortDescription = shortDescription;
        this.description = description;
        this.videoURL = videoURL;
        this.thumbnailURL = thumbnailURL;
    }

    public int getId() {
        return id;
    }

    public String getShortDescription() {
        return shortDescription;
    }

    public String getDescription() {
        return description;
    }

    public String getVideoURL() {
        return videoURL;
    }

    public String getThumbnailURL() {
        return thumbnailURL;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (!(o instanceof JsonStep)) return false;
        JsonStep step = (JsonStep) o;
        return getId() == step.getId() &&
                Objects.equals(getShortDescription(), step.getShortDescription()) &&
                Objects.equals(getDescription(), step.getDescription()) &&
                Objects.equals(getVideoURL(), step.getVideoURL()) &&
                Objects.equals(getThumbnailURL(), step.getThumbnailURL());
    }

    @Override
    public int hashCode() {

        return Objects.hash(getId(), getShortDescription(), getDescription(), getVideoURL(), getThumbnailURL());
    }

    @Override
    public String toString() {
        return "JsonStep{" +
                "id=" + id +
                ", shortDescription='" + shortDescription + '\'' +
                ", description='" + description + '\'' +
                ", videoURL='" + videoURL + '\'' +
                ", thumbnailURL='" + thumbnailURL + '\'' +
                '}';
    }

    // Parcelable implementation
    @Override
    public int describeContents() {
        return 0;
    }

    @Override
    public void writeToParcel(Parcel dest, int flags) {
        dest.writeInt(this.id);
        dest.writeString(this.shortDescription);
        dest.writeString(this.description);
        dest.writeString(this.videoURL);
        dest.writeString(this.thumbnailURL);
    }

    protected JsonStep(Parcel in) {
        this.id = in.readInt();
        this.shortDescription = in.readString();
        this.description = in.readString();
        this.videoURL = in.readString();
        this.thumbnailURL = in.readString();
    }

    public static final Creator<JsonStep> CREATOR = new Creator<JsonStep>() {
        @Override
        public JsonStep createFromParcel(Parcel source) {
            return new JsonStep(source);
        }

        @Override
        public JsonStep[] newArray(int size) {
            return new JsonStep[size];
        }
    };
}
